import { collection, addDoc } from 'firebase/firestore';
import { db } from '@/app/config/firebase';

export interface NewUser {
  email: string;
  createdAt?: Date;
}

export async function createUserInFirestore(user: NewUser) {
  return addDoc(collection(db, 'users'), {
    ...user,
    createdAt: new Date(),
  });
}
